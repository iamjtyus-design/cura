public final class MockQuickSendProvider: QuickSendProviding {
    public private(set) var copiedText: String?
    public var shouldOpenInstagram: Bool

    public init(shouldOpenInstagram: Bool = false) {
        self.shouldOpenInstagram = shouldOpenInstagram
    }

    public func copy(_ text: String) async {
        copiedText = text
#if canImport(UIKit)
        UIPasteboard.general.string = text
#endif
    }

    public func copyAndOpenInstagram(_ text: String) async -> QuickSendResult {
        await copy(text)

#if canImport(UIKit)
        if shouldOpenInstagram, let url = URL(string: "instagram://app"), UIApplication.shared.canOpenURL(url) {
            let opened = await withCheckedContinuation { continuation in
                UIApplication.shared.open(url, options: [:]) { success in
                    continuation.resume(returning: success)
                }
            }
            return QuickSendResult(didCopy: true, didOpenTargetApp: opened, shouldPresentShareSheet: !opened)
        }
#endif

        return QuickSendResult(didCopy: true, didOpenTargetApp: false, shouldPresentShareSheet: true)
    }
}
